# 功能介绍
# =============================================================================
st.divider()
st.subheader("✨ 功能详解")

tab1, tab2, tab3, tab4, tab5 = st.tabs(["💬 问答", "🔢 解题", "🗺 思维导图", "📝 AI 出题", "📋 历史记录"])

with tab1:
    st.markdown("""
**智能问答** — 基于你上传的资料回答问题

- 在学科详情页选择「问答」模式，直接输入问题
- 回答会标注引用的资料来源
- **资料不够用？** 勾选「结合通用知识」，AI 会同时使用自身知识库，并标注来源
- **有图片题目？** 点「📷 上传图片」或「📋 粘贴截图」，AI 自动识别文字后填入输入框
- 每次对话自动保存，可随时查看历史
""")

with tab2:
    st.markdown("""
**结构化解题** — 不只给答案，更教你思路

选择「解题」模式，输入题目，AI 会按以下结构输出：

| 部分 | 内容 |
|------|------|
| 考点 | 这道题考察的知识点 |
| 解题思路 | 分析思路和切入点 |
| 解题步骤 | 详细的解题过程 |
| 踩分点 | 容易丢分的地方 |
| 易错点 | 常见错误提醒 |

同样支持上传/粘贴图片题目，AI 自动识别后解题。
""")

with tab3:
    st.markdown("""
**思维导图** — 一键梳理全书知识结构

选择「思维导图」模式：
1. 选择资料范围（全部资料或单个文件）
2. 点击「生成思维导图」
3. 等待 AI 分析（约 30-60 秒）

生成的思维导图可以：
- 鼠标滚轮缩放
- 点击节点折叠/展开
- 点「导出 Markdown」下载源文件
- 想要图片格式？将导出的文件粘贴到 [markmap.js.org](https://markmap.js.org/repl)，右上角可导出 SVG/PNG

> 💡 建议：500 页以上的书生成时间较长，请耐心等待
""")

with tab4:
    st.markdown("""
**AI 出题** — 备考神器

在学科详情页切换到「🤖 AI 出题」标签：

**预测试卷**
- 有历年题时：AI 分析考点分布和题型规律，结合学科资料生成模拟试卷
- 没有历年题时：AI 直接基于已上传的学科资料生成综合模拟试卷
- 只要上传了资料就可以用，不强制要求历年题

**自定义出题**（无需历年题）
- 选择题型：选择题 / 填空题 / 简答题 / 计算题
- 设置数量（1-20 题）、难度、考点范围
- AI 基于你的资料出题，附详细参考答案

生成结果可导出为 Markdown 文件。
""")

with tab5:
    st.markdown("""
**对话历史** — 学习记录随时查看

- 所有问答、解题记录自动保存
- 在学科详情页点「📋 历史记录」查看本学科的历史
- 在侧边栏「对话历史」页查看所有学科的历史
- 支持按时间排序或按学科分组
- 每条记录可导出为 **Markdown / HTML / Word** 三种格式
""")

# =============================================================================
# 使用技巧
# =============================================================================
st.divider()
st.subheader("💡 使用技巧")

st.markdown("""
**上传资料**
- 文字版 PDF 处理最快，效果最好
- 扫描版 PDF 需先用 [ilovepdf.com](https://www.ilovepdf.com/zh-cn/pdf_to_word) 转成 Word 再上传
- 同一学科可以上传多个文件，AI 会综合所有资料回答

**提问技巧**
- 问题越具体，回答越准确（如「第三章中，巴拿赫空间的完备性如何证明？」）
- 资料里没有的内容，勾选「结合通用知识」
- 截图题目直接粘贴，比手打快很多

**思维导图**
- 先上传完整教材，再生成思维导图，覆盖章节更全
- 生成后可以重新生成，每次结果可能略有不同

**AI 出题**
- 上传多年历年题效果更好，考点分析更准确
- 自定义出题时，考点范围填得越具体越好
""")

# =============================================================================
# 常见问题
