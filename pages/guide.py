st.divider()
st.subheader("❓ 常见问题")

with st.expander("上传文件一直显示「处理中」怎么办？"):
    st.markdown("""
文件处理包括解析文本和向量化两步，500 页的书大约需要 5-15 分钟。
如果超过 30 分钟还没完成，可以删除该文件重新上传。
""")

with st.expander("为什么回答说「资料中没有相关内容」？"):
    st.markdown("""
可能原因：
1. 资料还在处理中（状态不是 ✅）
2. 问题涉及的内容在资料里确实没有
3. 问题描述和资料中的表述差异较大，可以换个说法

勾选「结合通用知识」可以让 AI 用自身知识补充回答。
""")

with st.expander("思维导图只有部分章节怎么办？"):
    st.markdown("""
重新生成一次，AI 每次采样的内容略有不同，多试几次通常能覆盖更多章节。
也可以选择单个文件生成，针对某一章节做更详细的思维导图。
""")

with st.expander("导出的 Word 文件打开乱码？"):
    st.markdown("用 Microsoft Word 或 WPS 打开，确保软件支持 .docx 格式。")
